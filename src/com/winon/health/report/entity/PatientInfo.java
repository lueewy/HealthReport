
package com.winon.health.report.entity;

/**
 * 病人信息
 */
public class PatientInfo {
	private String patientID;
	private String name;
	private String sex;
	private String offoce;
	private int age;
	private String idCertType;
	private String idCertID;
	private String address;
	private String guardianName;
	private String mobilePhone;
	private String journalID;
	private String residentID;
	public String getPatientID() {
		return patientID;
	}
	public void setPatientID(String patientID) {
		this.patientID = patientID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getOffoce() {
		return offoce;
	}
	public void setOffice(String offoce) {
		this.offoce = offoce;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getIdCertType() {
		return idCertType;
	}
	public void setIdCertType(String idCertType) {
		this.idCertType = idCertType;
	}
	public String getIdCertID() {
		return idCertID;
	}
	public void setIdCertID(String idCertID) {
		this.idCertID = idCertID;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getGuardianName() {
		return guardianName;
	}
	public void setGuardianName(String guardianName) {
		this.guardianName = guardianName;
	}
	public String getMobilePhone() {
		return mobilePhone;
	}
	public void setMobilePhone(String mobilePhone) {
		this.mobilePhone = mobilePhone;
	}
	public String getJournalID() {
		return journalID;
	}
	public void setJournalID(String journalID) {
		this.journalID = journalID;
	}
	public String getResidentID() {
		return residentID;
	}
	public void setResidentID(String residentID) {
		this.residentID = residentID;
	}
	
	
}
